package Abstractclass;

public class What_is_abstract_class {
    /*
    1. What is Abstract Class?
•	An abstract class is a class that cannot be instantiated and may contain both abstract and concrete methods.
•	It is used to achieve partial abstraction.
•	Key features
•	    1. Can not create object of abstract class.
•	    2. Can contain Abstract method
•	    3. Can contain concrete method
•	    4. Can have constructor
•	    5. can have instance variable

     2. Can we create object of abstract class?
•	No We Can not create object of abstract class.
     3.	Why abstract class cannot be instantiated?
•	An abstract class cannot be instantiated because it may contain abstract methods (methods without implementation), so the object would be incomplete.
Simple Explanation:
•	Abstract class = incomplete blueprint
•	It can have abstract methods (no body)
•	If we create an object → who will implement those methods? ❌
•	👉 That’s why Java does not allow creating objects of abstract class directly.


     */
}
